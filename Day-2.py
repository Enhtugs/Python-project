num1 = int(input("num1: "))
num2 = int(input("num2: "))
result1 = num1 + num2
result2 = num1 - num2
result3 = num1 * num2
result4 = num1 // num2

print("%d + %d = %d" %(num1, num2, result1))
print("%d - %d = %d" %(num1, num2, result2))
print("%d * %d = %d" %(num1, num2, result3))
print("%d / %d = %d" %(num1, num2, result4))

